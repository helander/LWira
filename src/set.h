#pragma once

#include <stdlib.h>

/* ========================================================================== */
/*                               Public Macros                                */
/* ========================================================================== */
#define SET_FOR_EACH(type, var, set_ptr) \
   for (size_t _i = 0; _i < (set_ptr)->count && ((var) = (type)(set_ptr)->items[_i], 1); ++_i)

/* ========================================================================== */
/*                              Public Typedefs                               */
/* ========================================================================== */
typedef struct {
   void **items;
   size_t count;
   size_t capacity;
} Set;

/* ========================================================================== */
/*                               Public State                                 */
/* ========================================================================== */

/* ========================================================================== */
/*                             Public Functions                               */
/* ========================================================================== */
void set_init(Set *set);

void set_free(Set *set);

void set_add(Set *set, void *item);

